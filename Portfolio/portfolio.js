document.querySelectorAll('a[href^="#"]').forEach(anchor => {
    anchor.addEventListener('click', function (e) {
        e.preventDefault();

        document.querySelector(this.getAttribute('href')).scrollIntoView({
            behavior: 'smooth'
        });
    });
});
const personalImage = document.getElementById('personal-image');

personalImage.addEventListener('mouseenter', () => {
    personalImage.classList.add('rotate');
});

personalImage.addEventListener('mouseleave', () => {
    personalImage.classList.remove('rotate');
});


